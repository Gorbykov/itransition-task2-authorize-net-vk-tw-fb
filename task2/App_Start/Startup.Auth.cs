            app.UseFacebookAuthentication(
               appId: "670219429798690",
               appSecret: "437572a50582cc41545cfd23a4eeca37");
            app.UseVkontakteAuthentication("5620808", "4BsCo5emmo3tqDdEraTF", "email");

